import Foundation

class Contact {
    var name: String!
    var address: String!
    var email: String!
    var phone: String!
    var company: String!
    
    init(name: String!, address: String!, email: String!, phone: String!, company: String!) {
        self.name = name
        self.address = address
        self.email = email
        self.phone = phone
        self.company = company
    }
}
