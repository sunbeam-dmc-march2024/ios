import Foundation

class Contact {
    var name: String!
    var address: String!
    var phone: String!
    var email: String!
    
    init(name: String!, address: String!, phone: String!, email: String!) {
        self.name = name
        self.address = address
        self.phone = phone
        self.email = email
    }
}
