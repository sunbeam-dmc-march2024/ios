import Foundation

class Car {
    var model: String!
    var company: String!
    var price: String!
    var color: String!
    var image: String!
    
    init(model: String!, company: String!, price: String!, color: String!, image: String!) {
        self.model = model
        self.company = company
        self.price = price
        self.color = color
        self.image = image
    }
}
